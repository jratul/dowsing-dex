export interface RoleRow {
  pokemon: string
  role: string
}

export interface CatchRow {
  timing: string
  pokemonId: number
  pokemon: string
  note: string
}

export interface MoveSetSection {
  pokemonId: number
  pokemon: string
  finalMoves: string[]
  moveTable: { move: string; usage: string }[]
  notes: string[]
}

export interface HmRow {
  hm: string
  move: string
  pokemon: string
  location: string
  badge: string
  note: string
}

export interface TmRow {
  tm: string
  move: string
  target: string
  location: string
  priority: string
  desc: string
}

export interface PartyStage {
  title: string
  members: string[]
  goals: string[]
}

export interface BossSection {
  title: string
  note: string
  rows: { opponent: string; answer: string }[]
}

export interface StarterGuideData {
  id: string
  starterPokemonId: number
  nameKo: string
  finalPartyIds: number[]
  finalRoles: RoleRow[]
  catchTable: CatchRow[]
  movesets: MoveSetSection[]
  stages: PartyStage[]
  gymMatchups: BossSection[]
  elite4: BossSection[]
  champion: BossSection
  summary: { stage: string; strategy: string }[]
  support: { pokemon: string; pokemonId: number; role: string; obtainedAt: string }[]
  cautions: string[]
}

